#ifndef PUSH_SWAP_H
# define PUSH_SWAP_H
# ifndef DEBUG
#  define DEBUG 0
# endif

# include "../lib/libft_printf.h"

//******************************************************************************
//			Typedefs
//******************************************************************************
typedef struct s_stack
{
	int				value;
	struct s_stack	*next;
}					t_stack;

void				ft_debug_printf(char *str, ...);

//******************************************************************************
//			Push
//******************************************************************************
void				pa(t_stack **a, t_stack **b);
void				pb(t_stack **a, t_stack **b);

//******************************************************************************
//			Swap
//******************************************************************************
void				sa(t_stack **a);
void				sb(t_stack **b);
void				ss(t_stack **a, t_stack **b);

//******************************************************************************
//			Rotate
//******************************************************************************
void				ra(t_stack **a);
void				rb(t_stack **b);
void				rr(t_stack **a, t_stack **b);

//******************************************************************************
//			Reverse Rotate
//******************************************************************************
void				rra(t_stack **a);
void				rrb(t_stack **b);
void				rrr(t_stack **a, t_stack **b);

#endif